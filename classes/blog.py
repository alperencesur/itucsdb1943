class Blog:
	def __init__(self, blogID, userID, tag, title, text, likeNum, dislikeNum, postedDate):
		self.blogID = blogID
		self.userID = userID
		self.tag = tag 
		self.title = title
		self.text = text
		self.likeNum = likeNum
		self.dislikeNum = dislikeNum
		self.postedDate = postedDate
